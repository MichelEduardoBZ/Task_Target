package Estrutura_Repeticao;

public class Program {

	public static void main(String[] args) {
		
		//Adicionando os valores necessário para fazer a estrutura
		
		int indice = 13, soma = 0, k = 0;
		
		// Estrutura
		
		while(k < indice) {
			
			k += 1;
			soma += k;
			
		}
		
		//Apresentação
		
		System.out.println("Soma: " + soma);
		
	}
	
}
